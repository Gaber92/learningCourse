mood = int(input("How are you feeling today?"))

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up!")
elif mood == "excited":
    print("Continue the hype train! Woooho!")
elif mood == "relaxed":
    print("Zen time.")
else:
    print("I don't recognize this mood")





